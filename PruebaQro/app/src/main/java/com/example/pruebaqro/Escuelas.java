package com.example.pruebaqro;

public class Escuelas {
    private int id_esc;
    private String nombre;

    public Escuelas() {
        this.id_esc = id_esc;
        this.nombre = nombre;
    }

    public int getId_esc() {
        return id_esc;
    }

    public void setId_esc(int id_esc) {
        this.id_esc = id_esc;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
}
