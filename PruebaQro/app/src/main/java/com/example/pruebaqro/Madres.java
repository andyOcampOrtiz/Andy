package com.example.pruebaqro;

public class Madres {
    private int id_mad;
    private String nombremadre;

    public Madres() {
        this.id_mad = id_mad;
        this.nombremadre = nombremadre;
    }

    public int getId_mad() {
        return id_mad;
    }

    public void setId_mad(int id_mad) {
        this.id_mad = id_mad;
    }

    public String getNombremadre() {
        return nombremadre;
    }

    public void setNombremadre(String nombremadre) {
        this.nombremadre = nombremadre;
    }
}
